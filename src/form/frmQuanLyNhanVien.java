    private String genderChose(){
        if(rbNam.isSelected()) {
            return "Nam";
        }
        else if(rbNu.isSelected()){
            return "Nữ";
        }
        else if(rbKhac.isSelected()){
            return "Khác";
        }
        return "";
    }
